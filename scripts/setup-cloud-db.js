#!/usr/bin/env node

/**
 * Cloud Database Setup Script for ShareWheelz
 * 
 * This script helps you set up a cloud PostgreSQL database
 * and configure your local environment to use it.
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

const question = (query) => new Promise((resolve) => rl.question(query, resolve));

async function setupCloudDatabase() {
  console.log('🌐 ShareWheelz Cloud Database Setup');
  console.log('=====================================\n');

  console.log('Choose your cloud database provider:');
  console.log('1. Supabase (Recommended - includes auth, real-time, storage)');
  console.log('2. Neon (Serverless PostgreSQL)');
  console.log('3. Railway (Simple deployment)');
  console.log('4. Custom PostgreSQL URL');
  console.log('5. Skip setup (use local database)\n');

  const choice = await question('Enter your choice (1-5): ');

  let databaseUrl = '';
  let sslConfig = {};

  switch (choice) {
    case '1':
      console.log('\n📋 Supabase Setup Instructions:');
      console.log('1. Go to https://supabase.com');
      console.log('2. Create a new project');
      console.log('3. Go to Settings > Database');
      console.log('4. Copy the connection string\n');
      
      databaseUrl = await question('Enter your Supabase connection string: ');
      sslConfig = {
        DB_SSL: 'true',
        DB_SSL_REJECT_UNAUTHORIZED: 'false'
      };
      break;

    case '2':
      console.log('\n📋 Neon Setup Instructions:');
      console.log('1. Go to https://neon.tech');
      console.log('2. Create a new project');
      console.log('3. Copy the connection string from dashboard\n');
      
      databaseUrl = await question('Enter your Neon connection string: ');
      sslConfig = {
        DB_SSL: 'true',
        DB_SSL_REJECT_UNAUTHORIZED: 'false'
      };
      break;

    case '3':
      console.log('\n📋 Railway Setup Instructions:');
      console.log('1. Go to https://railway.app');
      console.log('2. Create a new PostgreSQL service');
      console.log('3. Copy the connection string\n');
      
      databaseUrl = await question('Enter your Railway connection string: ');
      sslConfig = {
        DB_SSL: 'true',
        DB_SSL_REJECT_UNAUTHORIZED: 'false'
      };
      break;

    case '4':
      databaseUrl = await question('Enter your custom PostgreSQL connection string: ');
      sslConfig = {
        DB_SSL: 'true',
        DB_SSL_REJECT_UNAUTHORIZED: 'false'
      };
      break;

    case '5':
      console.log('\n✅ Skipping cloud database setup. Using local database.');
      rl.close();
      return;

    default:
      console.log('\n❌ Invalid choice. Exiting.');
      rl.close();
      return;
  }

  if (!databaseUrl) {
    console.log('\n❌ No connection string provided. Exiting.');
    rl.close();
    return;
  }

  // Create .env file
  const envPath = path.join(process.cwd(), '.env');
  const envContent = `# ShareWheelz Environment Configuration
# Generated by setup-cloud-db.js

# Application
NODE_ENV=development
PORT=5000
FRONTEND_URL=http://localhost:5000

# Cloud Database Configuration
DATABASE_URL=${databaseUrl}
${Object.entries(sslConfig).map(([key, value]) => `${key}=${value}`).join('\n')}

# JWT Authentication
JWT_SECRET=${generateRandomString(32)}
JWT_EXPIRES_IN=7d

# Stripe Payment Gateway
STRIPE_SECRET_KEY=sk_test_your_stripe_secret_key_here
STRIPE_PUBLISHABLE_KEY=pk_test_your_stripe_publishable_key_here
STRIPE_WEBHOOK_SECRET=whsec_your_webhook_secret_here

# Email Service (SMTP)
SMTP_HOST=smtp.gmail.com
SMTP_PORT=587
SMTP_SECURE=false
SMTP_USER=your-email@gmail.com
SMTP_PASS=your-app-password

# File Upload
MAX_FILE_SIZE=10485760
UPLOAD_DIR=public/uploads
OPTIMIZED_DIR=public/optimized
THUMBNAIL_DIR=public/thumbnails

# Image Optimization
IMAGE_QUALITY=85
THUMBNAIL_SIZE=300
RESPONSIVE_SIZES=400,800,1200,1600

# Rate Limiting
RATE_LIMIT_WINDOW_MS=900000
RATE_LIMIT_MAX_REQUESTS=100
AUTH_RATE_LIMIT_MAX_REQUESTS=5

# Security
BCRYPT_ROUNDS=12
SESSION_SECRET=${generateRandomString(32)}
CSRF_SECRET=${generateRandomString(32)}

# Demo User Password (for development)
DEMO_USER_PASSWORD=demo_password_123
`;

  try {
    fs.writeFileSync(envPath, envContent);
    console.log('\n✅ Environment file created successfully!');
    console.log(`📁 Location: ${envPath}`);
  } catch (error) {
    console.log('\n❌ Error creating environment file:', error.message);
    rl.close();
    return;
  }

  console.log('\n🚀 Next Steps:');
  console.log('1. Run database migrations: npm run migrate');
  console.log('2. Initialize sample data: npm run init-db');
  console.log('3. Start development server: npm run dev');
  console.log('\n💡 Your cloud database is now configured!');

  rl.close();
}

function generateRandomString(length) {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
  let result = '';
  for (let i = 0; i < length; i++) {
    result += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return result;
}

// Run the setup
setupCloudDatabase().catch(console.error);



















